{
	u32 i;
	
    //delay(30);
	TA5731_DATA_OUTPUT;
	
	for(i = 0;i < 8;i++)
	{
		if(Data & 0x80) TA5731_DATA_HIGH;
		else			TA5731_DATA_LOW;
		delay(30);
		TA5731_CLK_HIGH;
		delay(30);
		Data <<= 1;
		TA5731_CLK_LOW;
	}
}

