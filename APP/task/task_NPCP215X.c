bool Audio_ReceiveACK1(void)
{
    bool Ackflag = false;
	
	TA5731_DATA_INPUT;
    delay(30);
	TA5731_CLK_HIGH;
    delay(30);
	
	if(TA5731_DATA) 
    {   
        TA5731_DATA_OUTPUT;
		Ackflag = 1;
    }
	else 
		Ackflag = 0;
	
	TA5731_CLK_LOW;
		
    delay(30);

	return Ackflag;
}

