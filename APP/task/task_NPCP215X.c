void AudioCodec_Volume_Set(u8 function,bool Up_Down)					
{
    switch(function)
    {   
        case STATE_TREBLE:
            if(Up_Down)
			{
				sys_ctl.Trable_vol++;
			}
			else
			{
				sys_ctl.Trable_vol--; 
			}                       
			if(sys_ctl.Trable_vol >  10)sys_ctl.Trable_vol=	10;
			else if(sys_ctl.Trable_vol < -10)sys_ctl.Trable_vol= -10;
			Treble_Vol_Set();           
            break;
            
        case STATE_SUB:
            if(Up_Down)
			{
				sys_ctl.Sub_vol++;
			}
			else
			{
				sys_ctl.Sub_vol--; 
			}
			if(sys_ctl.Sub_vol > 32)sys_ctl.Sub_vol = 32;
			else if(sys_ctl.Sub_vol <  1)sys_ctl.Sub_vol = 0;
			else SW_Vol_Set();
            break;
            
        case STATE_BASS:
            if(Up_Down)
			{
				sys_ctl.Bass_vol++;	
			}
			else
			{
				sys_ctl.Bass_vol--;				   
			}            
			if(sys_ctl.Bass_vol >  10)sys_ctl.Bass_vol= 10;
			else if(sys_ctl.Bass_vol < -10)sys_ctl.Bass_vol=-10;
			Bass_Vol_Set();          
            break;
           
        default:
            if(Up_Down)
            {
                sys_ctl.Sub_vol++;
                sys_ctl.main_vol++;
                if(sys_ctl.main_vol > 32)sys_ctl.main_vol = 32;
            }
            else
            {
                sys_ctl.Sub_vol--;
                sys_ctl.main_vol--;
                if(sys_ctl.main_vol < 1)sys_ctl.main_vol = 0;
            }
            Main_Vol_Set();         
            if(sys_ctl.Sub_vol > 32)sys_ctl.Sub_vol = 32;
			else if(sys_ctl.Sub_vol <  1)sys_ctl.Sub_vol = 0;
			else SW_Vol_Set();                        
            if(sys_ctl.main_vol == 1)
        	{    	    
        		AudioCodec_MUTE(UNMUTE);
        	}
        	else if(sys_ctl.main_vol == 0) 
        	{   	    
        		AudioCodec_MUTE(MUTE);
        	}               
            if((sys_ctl.mute_flag)&&(sys_ctl.main_vol > 0))
            {
                sys_unmute(); 
                //Audio_Slow_Volume(UNMUTE);
                AudioCodec_MUTE(UNMUTE);
            }
            break;    
    }
    
    //Set_AudioCodec_MaxxBass(sys_ctl.main_vol);
}

