#pragma location="DSP_IIC_API1"
bool Audio_ReceiveACK(void)
{
    bool Ackflag = false;
	
	TA5731_DATA_INPUT;
    delay(30);
	TA5731_CLK_HIGH;
    delay(30);
	
	if(TA5731_DATA) 
    {   
        TA5731_DATA_OUTPUT;
		Ackflag = 1;
    }
	else 
		Ackflag = 0;
	
	TA5731_CLK_LOW;
		
    delay(30);

	return Ackflag;
}

#pragma location="DSP_IIC_API1"
void Audio_I2C_SendByte(u8 Data)
{
	u32 i;
	
    //delay(30);
	TA5731_DATA_OUTPUT;
	
	for(i = 0;i < 8;i++)
	{
		if(Data & 0x80) TA5731_DATA_HIGH;
		else			TA5731_DATA_LOW;
		delay(30);
		TA5731_CLK_HIGH;
		delay(30);
		Data <<= 1;
		TA5731_CLK_LOW;
	}
}
